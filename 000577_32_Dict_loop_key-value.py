# _000577_32_Dict_loop_key-value
customer_29876 = {"first name": "David",
                  "last name": "Elliott",
                  "address": "4803 Wellesley St."
                  }
for each_key, each_value in customer_29876.items():
    print("The customer's " + each_key + " is " + each_value)
# Note that a comma and space separate the two variables:
# Правильно, нужно обращать внимание на ньуансы
# And the keyword that comes before the empty parentheses changes. Now it's items.
