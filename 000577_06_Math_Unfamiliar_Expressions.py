# modulo
whats_left_over = 10 % 3
print("10 % 3 =", whats_left_over)

age = 12
age += 50
print("12 += 50:", age)

age = 12
age *= 3
print("12 *= 3:", age)

# Don't forget that you can always use a variable instead of a number
age = 12
amount_to_increment = 3
age += amount_to_increment
print("12 += 3:", age)

var1 = 3
var1 *= 2
print("3 *= 2:", var1)




