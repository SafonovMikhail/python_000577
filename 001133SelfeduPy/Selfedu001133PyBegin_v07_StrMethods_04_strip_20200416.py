print("   hello world         \n")
print("   hello world         \n".strip())
print('end')

print("# удаление пробелов переноса строки [справа] [rstrip], слева пробелы остались:")
print("   hello world         \n".rstrip())

print("# удаление пробелов [слева] [lstrip], справа пробелы и перенос строки остается:")
print("   hello world         \n".lstrip())
print('end')
