'''
описание фукнции [range]
'''

for i in 2, 3, 5:
    print(i * i)
print()
for i in range(10):
    print(i * i)
print()
for a in range(5): # от нуля до 4-х (выводится 5 элементов)
    print(a)
print()
for a in range (2,5): # интервал
    print (a)
print()
for a in range(2,15,4): # интервал с шагом
    print (a)
