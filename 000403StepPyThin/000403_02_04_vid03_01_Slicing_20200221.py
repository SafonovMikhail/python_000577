dna = '0123456789'

print('dna: \t\t\t', dna)
print('dna[1] \t\t\t', dna[1])
print('dna[1:4] \t\t', dna[1:4]) # левая граница не входит
print('dna[:4] \t\t', dna[:4])
print('dna[4:] \t\t', dna[4:])
print('dna[-4:] \t\t', dna[-4:])
print('dna[1:-1] \t\t', dna[1:-1])
print('dna[1:-1:2] \t', dna[1:-1:2]) # [-1] левая граница
print('dna[::-1] \t\t', dna[::-1]) # обращает

